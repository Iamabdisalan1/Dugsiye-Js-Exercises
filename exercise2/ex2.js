let x = 15;
let y = "15";
console.log(x == y);  // Output: true (loose equality)
console.log(x === y); // Output: false (strict equality)
console.log(x != y);  // Output: false
console.log(x !== y); // Output: true
console.log(x > y);   // Output: false
console.log(x < y);   // Output: false
console.log(x >= y);  // Output: true
console.log(x <= y);  // Output: true
